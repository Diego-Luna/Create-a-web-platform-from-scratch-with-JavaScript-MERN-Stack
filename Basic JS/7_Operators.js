// Binary operators:
console.log( 3 + 2 ); // Sum
console.log( 50 - 10 ); // Subtraction
console.log( 10 * 20 ); // Multiplication
console.log( 2/20 ); // Division

console.log( "Diego" + "Luna" ); // string concatenation
console.log("-------------------");

// Unitary operators:
console.log( ! false ); // negation of negation = true
console.log("-------------------");

// Assignment operators:
var a = 1; // We assign a value to the variable

console.log(a);
console.log("-------------------");

// Operators to compare:
console.log( 3 == "3"); // Compare the values and return "true" in this case

console.log( 3 === "3"); // Compare and validate the types and values. Returns "false" in this case

console.log(5 < 3); // Compare and validate if 5 is less than 3
console.log(5 > 3); // Compare and validate if 5 is greater than 3
console.log(5 <= 6); // Compare and validate if 5 is less than or equal to 6
console.log(5 >= 6); // Compare and validate if 5 is greater than or equal to 6
console.log("-------------------");

const a = (5 < 3)
const b = (5 > 3)

console.log( a && b ); // Validate if both variables are true for the condition to be met
console.log( a || b ); // Here the condition is fulfilled if any of the two variables is true
console.log("-------------------");

