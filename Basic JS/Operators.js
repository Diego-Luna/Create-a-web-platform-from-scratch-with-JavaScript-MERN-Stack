3 + 2 // Sum
50 - 10 // Subtraction
10 * 20 // Multiplication
2/20 // Division

"Diego" + "Luna" // string concatenation

// Unitary operators:
! false // negation of negation = true

// Assignment operators:
var a = 1; // We assign a value to the variable

// Operators to compare:
3 == "3"; // Compare the values and return "true" in this case

3 === "3"; // Compare and validate the types and values. Returns "false" in this case

5 <3 // Compare and validate if 5 is less than 3
5> 3 // Compare and validate if 5 is greater than 3
5 <= 6 // Compare and validate if 5 is less than or equal to 6
5 >= 6 // Compare and validate if 5 is greater than or equal to 6

a && b // Validate if both variables are true for the condition to be met
a || b // Here the condition is fulfilled if any of the two variables is true

var age = 40
age ++ // Increase the value by 1

age += 2 // Increase the value by 2